    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        String physics=jTextField2.getText();
        String chemistry=jTextField3.getText();
        String maths=jTextField4.getText();
        String em=jTextField5.getText();
        String dbms=jTextField6.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/student_data", "root", "sqlpass");
            
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where Roll_No='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("Insert into result(Roll_No,Physics,Chemistry,Maths,EM,DBMS) values('"+rollno+"','"+physics+"','"+chemistry+"','"+maths+"','"+em+"','"+dbms+"')");
                JOptionPane.showMessageDialog(null, "Successfully added data.");
                setVisible(false);
                new insertNewResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"This Roll_No does not exist.");
            }
            
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(null,e.toString());
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

